package com.oracle.hcm.model;

public class Profile {

	private String product_start_date;
	private String product_end_date;
	private String product;
	private String role;
	private String func_start_date;
	private String func_end_date;
	private String function;
	private String loc_start_date;
	private String loc_end_date;
	private String city;

	public String getProduct_start_date() {
		return product_start_date;
	}

	public void setProduct_start_date(String product_start_date) {
		this.product_start_date = product_start_date;
	}

	public String getProduct_end_date() {
		return product_end_date;
	}

	public void setProduct_end_date(String product_end_date) {
		this.product_end_date = product_end_date;
	}

	public String getProduct() {
		return product;
	}

	public void setProduct(String product) {
		this.product = product;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

	public String getFunc_start_date() {
		return func_start_date;
	}

	public void setFunc_start_date(String func_start_date) {
		this.func_start_date = func_start_date;
	}

	public String getFunc_end_date() {
		return func_end_date;
	}

	public void setFunc_end_date(String func_end_date) {
		this.func_end_date = func_end_date;
	}

	public String getFunction() {
		return function;
	}

	public void setFunction(String function) {
		this.function = function;
	}

	public String getLoc_start_date() {
		return loc_start_date;
	}

	public void setLoc_start_date(String loc_start_date) {
		this.loc_start_date = loc_start_date;
	}

	public String getLoc_end_date() {
		return loc_end_date;
	}

	public void setLoc_end_date(String loc_end_date) {
		this.loc_end_date = loc_end_date;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

}
